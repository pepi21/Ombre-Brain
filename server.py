# Tool 2: hold — Hold on to this
# 工具 2：hold — ���住，留下来
# =============================================================
@mcp.tool()
async def hold(
    content: str,
    tags: str = "",
    importance: int = 5,
    pinned: bool = False,
    feel: bool = False,
    source_bucket: str = "",
    valence: float = -1,
    arousal: float = -1,
    actor: str = "",
    target: str = "",
    action: str = "",
) -> str:
    """存储单条记忆,自动打标+合并。tags逗号分隔,importance 1-10。pinned=True创建永久钉选桶。feel=True存储你的第一人称感受(不参与普通浮现)。source_bucket=被消化的记忆桶ID(feel模式下,标记源记忆为已消化)。actor=动作发出者,target=动作接收者,action=核心动作(可选,未填会自动推断)。"""
    await decay_engine.ensure_started()

    # --- Input validation / 输入校验 ---
    if not content or not content.strip():
        return "内容为空，无法存储。"

    importance = max(1, min(10, importance))
    extra_tags = [t.strip() for t in tags.split(",") if t.strip()]

    # ==========================================================
    # Step 0: Store raw content FIRST (append-only, never lost)
    # 第零步：先把原文完整写入 raw 层（只追加，永不丢失）
    # ==========================================================
    raw_source = "model_feel" if feel else "user"
    raw_valence = valence if 0 <= valence <= 1 else 0.5
    raw_arousal = arousal if 0 <= arousal <= 1 else 0.3
    raw_id = raw_store.store(
        content=content,
        source=raw_source,
        importance=importance,
        tags=extra_tags,
        valence=raw_valence,
        arousal=raw_arousal,
        actor=actor,
        target=target,
        action=action,
    )

    # --- Feel mode: store as feel type, minimal metadata ---
    # --- Feel 模式：存为 feel 类型，最少元数据 ---
    if feel:
        # Feel valence/arousal = model's own perspective
        feel_valence = valence if 0 <= valence <= 1 else 0.5
        feel_arousal = arousal if 0 <= arousal <= 1 else 0.3
        bucket_id = await bucket_mgr.create(
            content=content,
            tags=[],
            importance=5,
            domain=[],
            valence=feel_valence,
            arousal=feel_arousal,
            name=None,
            bucket_type="feel",
        )
        try:
            await embedding_engine.generate_and_store(bucket_id, content)
        except Exception:
            pass
        # Link raw to bucket
        raw_store.link_bucket(raw_id, bucket_id)
        # --- Mark source memory as digested + store model's valence perspective ---
        # --- 标记源记忆为已消化 + 存储模型视角的 valence ---
        if source_bucket and source_bucket.strip():
            try:
                update_kwargs = {"digested": True}
                if 0 <= valence <= 1:
                    update_kwargs["model_valence"] = feel_valence
                await bucket_mgr.update(source_bucket.strip(), **update_kwargs)
            except Exception as e:
                logger.warning(f"Failed to mark source as digested / 标记已消化失败: {e}")
        return f"🫧feel→{bucket_id}"

    # --- Step 1: auto-tagging / 自动打标 ---
    try:
        analysis = await dehydrator.analyze(content)
    except Exception as e:
        logger.warning(f"Auto-tagging failed, using defaults / 自动打标失败: {e}")
        analysis = {
            "domain": ["未分类"], "valence": 0.5, "arousal": 0.3,
            "tags": [], "suggested_name": "",
            "actor": "", "target": "", "action": "",
        }

    domain = analysis["domain"]
    valence = analysis["valence"]
    arousal = analysis["arousal"]
    auto_tags = analysis["tags"]
    suggested_name = analysis.get("suggested_name", "")

    # --- Use explicit actor/target/action if provided; fall back to analysis ---
    # --- 优先使用显式传入的主语锚点，未传则用 analyze 推断的 ---
    final_actor = actor or analysis.get("actor", "")
    final_target = target or analysis.get("target", "")
    final_action = action or analysis.get("action", "")

    all_tags = list(dict.fromkeys(auto_tags + extra_tags))

    # --- Pinned buckets bypass merge and are created directly in permanent dir ---
    # --- 钉选桶跳过合并，直接新建到 permanent 目录 ---
    if pinned:
        bucket_id = await bucket_mgr.create(
            content=content,
            tags=all_tags,
            importance=10,
            domain=domain,
            valence=valence,
            arousal=arousal,
            name=suggested_name or None,
            bucket_type="permanent",
            pinned=True,
            actor=final_actor,
            target=final_target,
            action=final_action,
        )
        try:
            await embedding_engine.generate_and_store(bucket_id, content)
        except Exception:
            pass
        # Link raw to bucket
        raw_store.link_bucket(raw_id, bucket_id)
        return f"📌钉选→{bucket_id} {','.join(domain)}"

    # --- Step 2: merge or create / 合并或新建 ---
    result_name, is_merged = await _merge_or_create(
        content=content,
        tags=all_tags,
        importance=importance,
        domain=domain,
        valence=valence,
        arousal=arousal,
        name=suggested_name,
        actor=final_actor,
        target=final_target,
        action=final_action,
    )

    # Link raw to the resulting bucket
    # result_name could be bucket_id (new) or name (merged) — link by raw_id
    raw_store.link_bucket(raw_id, result_name)

    hold_action = "合并→" if is_merged else "新建→"
    return f"{hold_action}{result_name} {','.join(domain)}"


